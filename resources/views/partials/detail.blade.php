<h3>
Nějaký nadpis
</h3>
<p>
Provozujete internetový obchod a hledáte způsoby, jak zvýšit počty prodaných kusů nabízeného zboží? Stejně jako v jiných odvětvích i zde existuje spousta všelijakých cest, jak toho dosáhnout – vaše úsilí je možné zaměřit například na zvýšení návštěvnosti stránek (zákazník, který o vás neví, si u vás stoprocentně nic nekoupí), prodávat nabízené zboží levněji než konkurence, poskytovat zákazníkům zajímavé slevy (či nějaký věrnostní program), nabízet zákazníkům nějakou přidanou hodnotu atd. atd. Tohle všechno jsou obvyklé postupy. Ale co zkusit něco jiného? Jistou inspirací může být článek E-Commerce Copywriting: The Guide to Selling More, který se zaměřuje na poněkud opomíjenou oblast optimalizace – popis zboží. Kvalitní popisek totiž může vést ke značnému nárůstu prodeje – nikdo nechce číst unifikované popisky zboží pocházejících přímo od výrobce.
</p>
<h3>Další nadpis</h3>
<p>
Na začátku podnikání hledáme cesty, jak bychom si usnadnili podnikání a dostali se zkratkou tam, kam chceme. Napojení e-shopu na XML feed od dodavatele je proto pro nejednoho začínajícího podnikatele vykoupením. Ušetří moře času a námahy.
</p>
<p>
